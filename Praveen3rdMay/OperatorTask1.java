package Praveen3rdMay;

public class OperatorTask1
{
	public static void main(String[] args) 
	{
		
		int a = 9;

		a++;              //a=10

		System.out.println (a);        			 //10

		a--;		    //a=9

		System.out.println (a);         		  //9

		++a;			//a=10

		System.out.println (a);         		  //10

		System.out.println (a++);       		  //10

						//a=10++ ,a= 11
					
		System.out.println (a--);     			  //11
		
						//a=11-- ,a= 10

		System.out.println (--a);   			  //9

		System.out.println (++a);  				  //10
		
		
	}

}
